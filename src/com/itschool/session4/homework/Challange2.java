package com.itschool.session4.homework;

public class Challange2 {
    public static void main(String[] args) {
        /*
            Create a Java program where you increment and decrement the same variable.
            Display the value of the variable after each operation.
         */

        int number = 1;

        System.out.println("Value after increment: " + ++number);
        System.out.println("Value after decrement: " + --number);
    }
}
